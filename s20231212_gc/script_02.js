// 基本資料型態&變數

// 1.字串，用單引號或雙引號 夾文字數字
document.write("有一個人叫小白");
document.write("<br>");

// 2.數字

// 3.布林值 true or false

// 4.變數，是一種容器，可以存放各種資料，方便管理各種資料。
// 變數命名規則一:只能是英文大小寫，數字或是$或_的組合
// 規則二:開頭不能是數字
// 定義變數用var
var my_name = "小白";  //存放字串型態
var my_age = 87;  //存放數字型態
var is_male = true;  //存放布林值

document.write("有一個人叫"+ my_name);
document.write("<br>");
document.write("我的年紀是"+ my_age +"歲");
document.write("<br>");
document.write(is_male);
